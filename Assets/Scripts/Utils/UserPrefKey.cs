using System.Collections;
using System.Collections.Generic;
using UnityEngine;

/// <summary>
/// Class to store the different keys used to store the user preferences.
/// </summary>
public class UserPrefKey
{
    /// <summary>
    /// Key for storing if the user wants to be a host.
    /// </summary>
    public static string IS_HOST = "IS_HOST";

    /// <summary>
    /// Key for storing the server's IP address.
    /// </summary>
    public static string SERVER_IP = "SERVER_IP";

    /// <summary>
    /// Key for storing the server's port.
    /// </summary>
    public static string SERVER_PORT = "SERVER_PORT";

    /// <summary>
    /// Key for storing the user's name.
    /// </summary>
    public static string USER_NAME = "USER_NAME";
}
